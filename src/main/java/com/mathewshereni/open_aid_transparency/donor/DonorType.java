package com.mathewshereni.open_aid_transparency.donor;

// enum is a fixed set of allowed values. Only what we defined bellow is what will be accepted
public enum DonorType {
    COUNTRY,        // a national government, e.g. United States, Norway
    MULTILATERAL,   // pooled-funds body, e.g. World Bank, African Development Bank
    NGO,            // non-governmental organisation
    FOUNDATION,     // private foundation, e.g. Gates Foundation

}
